const scamKeywords: string[] = [
	'скинь деньги',
	'номер карты',
	'пароль',
	'перевод на карту',
	'скинь реквизиты',
	'отправь деньги',
	'код подтверждения',
	'верификация',
	'восстановление пароля',
	'получить доступ',
	'восстановить аккаунт',
	'кликни на ссылку',
	'зайди на сайт',
	'ссылка на страницу',
	'выиграл',
	'получите подарок',
	'приз',
	'пожертвования',
	'спонсорская помощь',
	'письмо с угрозами',
]

export function checkForScam(text: string): string | null {
	for (const keyword of scamKeywords) {
		if (text.toLowerCase().includes(keyword)) {
			return `⚠️ Найдено подозрительное слово: "${keyword}". Будьте осторожны, возможны мошенники! ⚠️`
		}
	}
	return null
}
